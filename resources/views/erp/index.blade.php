        $(window).on('scroll', function(){
            if ($(window).scrollTop()){
                $('nav').addClass('black');               
            }
            else
            {
                $('nav').removeClass('black');
            }               
        });

        let silderImages = document.querySelectorAll('.slide'),
            arrowRight = document.querySelector('#arrow-right'),
            arrowLeft = document.querySelector('#arrow-left'),
            current = 0;

        //Clear all images
        function reset(){
            for(let i = 0; i <silderImages.length; i++){
                silderImages[i].style.display = 'none';
            }
        }

        // Init slider
        function startSlide(){
            reset();
            silderImages[0].style.display = 'block';
        }

        //show prev
        function slideLeft(){
            reset();
            silderImages[current - 1].style.display = 'block';
            current--;
        }

        //show next
        function slideRight(){
            reset();
            silderImages[current + 1].style.display = 'block';
            current++;
        }

        //left arrow click
        arrowLeft.addEventListener('click', function(){
            if(current === 0){
                current = silderImages.length;
            }
            slideLeft();
        });

        //right arrow click
        arrowRight.addEventListener('click', function(){
            if(current === silderImages.length - 1){
                current = -1
            }
            slideRight();
        });



        startSlide();             
    </script>
        
